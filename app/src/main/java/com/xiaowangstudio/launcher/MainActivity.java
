package com.xiaowangstudio.launcher;

import android.os.Bundle;
import android.view.View;
import android.widget.Button;
import android.widget.Toast;
import androidx.appcompat.app.AppCompatActivity;

public class MainActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        // 找到各个按钮
        Button btnStartGame = findViewById(R.id.btn_start_game);
        Button btnDownloadVersion = findViewById(R.id.btn_download_version);
        Button btnAiDiagnosis = findViewById(R.id.btn_ai_diagnosis);
        Button btnSettings = findViewById(R.id.btn_settings);

        // 启动游戏点击事件
        btnStartGame.setOnClickListener(v -> {
            Toast.makeText(this, "正在启动我的世界...", Toast.LENGTH_SHORT).show();
            // 这里可以后续添加启动游戏的逻辑
        });

        // 下载版本点击事件
        btnDownloadVersion.setOnClickListener(v -> {
            Toast.makeText(this, "打开版本下载界面", Toast.LENGTH_SHORT).show();
            // 这里可以后续添加下载逻辑
        });

        // AI诊断点击事件
        btnAiDiagnosis.setOnClickListener(v -> {
            Toast.makeText(this, "正在进行AI诊断...", Toast.LENGTH_SHORT).show();
            // 这里可以后续添加模组/光影检测逻辑
        });

        // 设置点击事件
        btnSettings.setOnClickListener(v -> {
            Toast.makeText(this, "打开设置界面", Toast.LENGTH_SHORT).show();
            // 这里可以后续添加设置界面跳转逻辑
        });
    }
}
